type HintCopy = readonly [goal: string, trigger: string, setup: string, nextMove: string];

function hints(topic: string, owner: string, copy: HintCopy): ProgressiveHint[] {
  return (["goal", "trigger", "setup", "next_move"] as const).map((kind, index) => ({
    id: `hint.q86.algebraic-translation.${topic}.${owner}.${kind.replace("_", "-")}`,
    kind,
    textMd: copy[index],
  }));
}

function example(
  topic: string,
  slug: string,
  conceptIds: readonly string[],
  authoredDifficulty: SegmentExample["authoredDifficulty"],
  role: SegmentExample["role"],
  questionMd: string,
  intendedMethod: string,
  answer: AnswerSpec,
  answerLabelMd: string,
  solutionMd: string,
  hintCopy: HintCopy,
): SegmentExample {
  return {
    id: `example.q86.algebraic-translation.${topic}.${slug}`,
    conceptIds,
    authoredDifficulty,
    role,
    questionMd,
    intendedMethod,
    answer,
    answerLabelMd,
    solutionMd,
    hints: hints(topic, `example-${slug}`, hintCopy),
  };
}

