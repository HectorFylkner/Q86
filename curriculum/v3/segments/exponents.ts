const E = PILOT_CONCEPT_IDS.exponents;
const ZERO_PRODUCT = conceptId("quadratics_factoring", "Zero-product property");
const PARITY_POWERS = conceptId("parity_signs", "Powers preserve parity");
const SOURCE_PATH = "curriculum/v3/segments/exponents.ts" as const;

type ExponentKey = keyof typeof E;
type HintCopy = Record<ProgressiveHint["kind"], string>;
type ExampleSpec = Omit<SegmentExample, "id" | "conceptIds" | "hints"> & {
  slug: string;
  conceptIds?: readonly string[];
  hintCopy: HintCopy;
};
type CheckSpec = Omit<RetrievalCheck, "id" | "conceptIds" | "hints"> & {
  slug: string;
  conceptIds?: readonly string[];
  hintCopy: HintCopy;
};
type PrerequisiteSpec = Omit<PrerequisiteCheck, "id"> & { slug: string };
type MisconceptionSpec = Omit<SegmentMisconception, "id"> & { slug: string };
type SegmentSpec = Omit<
  ConceptSegment,
  "conceptId" | "contentVersion" | "sourcePath" | "prerequisiteChecks" |
    "examples" | "contrastPair" | "misconceptions" | "checks"
> & {
  prerequisiteChecks: readonly PrerequisiteSpec[];
  examples: readonly ExampleSpec[];
  contrastPair: Omit<ConceptSegment["contrastPair"], "id"> & { slug: string };
  misconceptions: readonly MisconceptionSpec[];
  checks: readonly CheckSpec[];
};

function keySlug(key: ExponentKey): string {
  return key.replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`);
}

function stableId(
  kind: "example" | "check" | "prereq" | "contrast" | "misconception",
  key: ExponentKey,
  slug: string,
): string {
  return `${kind}.q86.exponents.${keySlug(key)}.${slug}`;
}

function hints(key: ExponentKey, owner: string, copy: HintCopy): ProgressiveHint[] {
  return (['goal', 'trigger', 'setup', 'next_move'] as const).map((kind) => ({
    id: `hint.q86.exponents.${keySlug(key)}.${owner}.${kind.replace('_', '-')}`,
    kind,
    textMd: copy[kind],
  }));
}

function buildSegment(key: ExponentKey, spec: SegmentSpec): ConceptSegment {
  return {
    conceptId: E[key],
    contentVersion: "3.0.0",
    sourcePath: SOURCE_PATH,
    objective: spec.objective,
    prerequisiteChecks: spec.prerequisiteChecks.map(({ slug, ...item }) => ({
      id: stableId("prereq", key, slug),
      ...item,
    })),
    intuitiveModelMd: spec.intuitiveModelMd,
    formalRuleMd: spec.formalRuleMd,
    procedure: spec.procedure,
    examples: spec.examples.map(({ slug, conceptIds, hintCopy, ...item }) => ({
      id: stableId("example", key, slug),
      conceptIds: conceptIds ?? [E[key]],
      hints: hints(key, `example-${slug}`, hintCopy),
      ...item,
    })),
    contrastPair: {
      id: stableId("contrast", key, spec.contrastPair.slug),
      caseAMd: spec.contrastPair.caseAMd,
      caseBMd: spec.contrastPair.caseBMd,
      explanationMd: spec.contrastPair.explanationMd,
    },
    misconceptions: spec.misconceptions.map(({ slug, ...item }) => ({
      id: stableId("misconception", key, slug),
      ...item,
    })),
    checks: spec.checks.map(({ slug, conceptIds, hintCopy, ...item }) => ({
      id: stableId("check", key, slug),
      conceptIds: conceptIds ?? [E[key]],
      hints: hints(key, `check-${slug}`, hintCopy),
      ...item,
    })),
    speedMethod: spec.speedMethod,
    recapMd: spec.recapMd,
    retrievalPrompts: spec.retrievalPrompts,
  };
}

